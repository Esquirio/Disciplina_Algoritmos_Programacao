#include <stdio.h>  // Funcoes de I/O
/* Funcao main() */
int main() {
  int pontos;     // Declara variáveis int
  pontos = 1000;  // Atribui valor a variavel
  // Comando if para realizar o teste logico
  if (pontos >= 1000) {
    // Acao executada se o teste logico for verdadeiro
    printf("Voce passou para a fase 2! \n");
  }  // Fim do if
  return 0;
}  // Fim da funcao main()
