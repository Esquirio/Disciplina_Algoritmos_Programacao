#include <stdio.h>  // Funções de I/O
/* Função main() inicia o programa */
int main() {
  // Declaração de variáveis
  int idade = 20;
  float peso = 68.7;
  char sexo = 'm';
  // Imprimindo valores
  printf("Idade: %d\n", idade);
  printf("Peso: %f\n", peso);
  printf("Sexo: %c\n\n", sexo);
  return 0;
}  // Fim da função main()
